package SWT;

public class SWTPoint2d {

	
	
	int x;
    int y;
    float SWT;
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public float getSWT() {
		return SWT;
	}
	public void setSWT(float sWT) {
		SWT = sWT;
	}
    
    
    
    
}
